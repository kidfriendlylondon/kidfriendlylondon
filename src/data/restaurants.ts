export function getRestaurantsByNeighbourhood(neighbourhood: Neighbourhood): Restaurant[] {
  return restaurants.filter((r) => r.neighbourhood === neighbourhood);
}

export function getRestaurantsByBorough(borough: Borough): Restaurant[] {
  return restaurants.filter((r) => r.borough === borough);
}

export function getFeaturedRestaurants(): Restaurant[] {
  return restaurants.filter((r) => r.featured);
}

export function getRelatedRestaurants(restaurant: Restaurant, limit = 3): Restaurant[] {
  return restaurants
    .filter(
      (r) =>
        r.id !== restaurant.id &&
        (r.neighbourhood === restaurant.neighbourhood ||
          r.cuisineType === restaurant.cuisineType)
    )
    .slice(0, limit);
}

export function generateMetaDescription(restaurant: Restaurant): string {
  const attrs: string[] = [];
  if (restaurant.kidsMenu === "yes") attrs.push("kids menu");
  if (restaurant.outdoorSpace === "yes") attrs.push("outdoor space");
  if (restaurant.highchairs === "yes") attrs.push("highchairs");
  if (restaurant.softPlay === "yes") attrs.push("soft play");
  if (restaurant.buggyAccessible === "yes") attrs.push("buggy accessible");

  const neighbourhood = neighbourhoodMeta[restaurant.neighbourhood].name;
  const attrsStr = attrs.length > 0 ? `, with ${attrs.slice(0, 3).join(", ")}` : "";
  const booking =
    restaurant.bookingRequired === "yes"
      ? "Booking required."
      : restaurant.bookingRequired === "recommended"
        ? "Booking recommended."
        : "Walk-ins welcome.";

  return `${restaurant.name} in ${neighbourhood} is ${restaurant.cuisineType}, ${restaurant.priceRange}${attrsStr}. Rated ${restaurant.googleRating}/5 on Google from ${restaurant.reviewCount} reviews. ${booking}`;
}
